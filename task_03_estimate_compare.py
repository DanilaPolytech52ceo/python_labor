# Исходная температура в градусах Цельсия
temp_c = -8

# Получаем значение по шкале Фаренгейта
temp_f = temp_c * 9 / 5 + 32

# Проверяем агрегатное состояние воды
if temp_c <= 0:
    water_form = "Лёд"
elif temp_c >= 100:
    water_form = "Пар"
else:
    water_form = "Жидкость"

# Показываем результаты перевода температуры и анализа состояния воды
print("=== КОНВЕРТЕР ТЕМПЕРАТУР ===")
print(f"Температура в Цельсиях: {temp_c} °C")
print(f"Температура в Фаренгейтах: {temp_f:.2f} °F")
print(f"Состояние воды: {water_form}")
