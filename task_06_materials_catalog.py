# Начальный перечень материалов
catalog = ["Газоблок", "Штукатурка", "Клей", "Плитка", "Грунтовка"]

# Показываем исходное состояние списка и примеры доступа по индексам
print("=== КАТАЛОГ МАТЕРИАЛОВ ===")
print(f"Исходный список: {catalog}")
print(f"Первый материал: {catalog[0]}")
print(f"Последний материал: {catalog[-1]}")
print(f"Средние элементы: {catalog[1:4]}")

# Добавляем ещё две позиции в конец списка
catalog.append("Шпаклёвка")
catalog.append("Ламинат")

print()
print("После добавления двух материалов:")
print(catalog)

# Убираем второй элемент каталога
deleted_item = catalog.pop(1)

# Печатаем обновлённый каталог после удаления элемента
print()
print(f"Удалённый второй элемент: {deleted_item}")
print(f"Итоговый список: {catalog}")
print(f"Длина списка: {len(catalog)}")
